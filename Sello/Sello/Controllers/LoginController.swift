    func isValid(char: Character) -> Bool{
        if(char >= "A" && char <= "Z"){
            return true
        }
        if(char >= "a" && char <= "z"){
            return true
        }
        if(char >= "0" && char <= "9"){
            return true
        }
        if(char == "." || char == "+" || char == "_"){
            return true
        }
        return false
    }
    func checkUser(username: String) -> error{
        var after = false
        var first = true
        var prev: Character = "A"
        for char in username{
            if(first){
                prev = char
                first = false
            }
            if(!after){
                if(char == "@"){
                    after = true
                }
                else{
                    if(!isValid(char: char)){
                        return error.invalidChar
                    }
                    if(char == "." && char == prev){
                        return error.invalidChar
                    }
                }
            }
            else{
                if(char == "@"){
                    return error.invalidChar
                }
                else{
                    if(!isValid(char: char)){
                        return error.invalidChar
                    }
                    if(char == "." && char == prev){
                        return error.invalidChar
                    }
                }
            }
            prev = char
        }
        let last = username.suffix(4)
        if(last != ".edu"){
            return error.notUniv
        }
        return error.success
    }
